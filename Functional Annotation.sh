#Protein-Protein Interaction (PPI) network analysis
We used https://string-db.org/ databases and cytoscape application and plugin CytoHubba 
#Regulatory and resistance gene identification 
we used online https://planttfdb.gao-lab.org/prediction.php for transcription factor identification of DEGs genes 
We used online https://www.prgdb.org/prgdb4/drago3 tool for resistant genes identification of DEGs genes
#Functional annotations of significantly expressed genes
we used online ShinyGo tool https://bioinformatics.sdstate.edu/go/ for functional annotations of significant genes
